function applyTranslations(lang) {
    localStorage.setItem('taplito_lang', lang);
    
    // Update active lang pill
    document.querySelectorAll('.lang-pill').forEach(pill => {
        pill.classList.toggle('active', pill.getAttribute('data-lang') === lang);
    });

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName.toLowerCase() === 'input' || el.tagName.toLowerCase() === 'textarea') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Special cases for mixed content
    document.querySelectorAll('[data-i18n-mixed]').forEach(el => {
        const type = el.getAttribute('data-i18n-mixed');
        if (type === 'slide-pay') {
            const currentTotal = document.getElementById('totalDisplay') ? document.getElementById('totalDisplay').textContent : '17.25€';
            el.textContent = `${translations[lang]['slide_pay']} · ${currentTotal}`;
        }
        if (type === 'checkout-btn-cart') {
            el.textContent = `${translations[lang]['checkout_continue']} · 15.00€`;
        }
    });
}

function getTranslation(key) {
    const lang = localStorage.getItem('taplito_lang') || 'hr';
    return translations[lang][key] || key;
}

// Global initialization
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('taplito_lang') || 'hr';
    applyTranslations(savedLang);

    // Rebind all language pills to use global translation
    document.body.addEventListener('click', (e) => {
        if (e.target.classList.contains('lang-pill')) {
            const lang = e.target.getAttribute('data-lang');
            applyTranslations(lang);
            
            // Show toast
            const toast = document.createElement('div');
            toast.className = 'toast';
            toast.textContent = getTranslation('lang_changed');
            document.body.appendChild(toast);
            setTimeout(() => toast.remove(), 2000);
        }
    });
});
